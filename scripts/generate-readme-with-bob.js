// Run with: npm run bob:readme
// Generates README.bob-generated.md using IBM Bob based on actual project code.
const fs = require('fs');
const path = require('path');
const zlib = require('zlib');

// Load .env.local manually (no dotenv dependency needed)
const envPath = path.join(__dirname, '..', '.env.local');
try {
  const lines = fs.readFileSync(envPath, 'utf-8').split('\n');
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eqIdx = trimmed.indexOf('=');
    if (eqIdx < 0) continue;
    const key = trimmed.slice(0, eqIdx).trim();
    const value = trimmed.slice(eqIdx + 1).trim();
    if (key && !process.env[key]) process.env[key] = value;
  }
} catch {
  console.error('Could not read .env.local — run this script from reporadar/');
  process.exit(1);
}

const BOB_ENDPOINT = process.env.IBM_BOB_ENDPOINT || 'https://bob.ibm.com/v1';
const BOB_API_KEY = process.env.IBM_BOB_API_KEY;
const BOB_MODEL = process.env.IBM_BOB_MODEL || 'bob-v1';

if (!BOB_API_KEY) {
  console.error('IBM_BOB_API_KEY is not set in .env.local');
  process.exit(1);
}

const SKIP_DIRS = new Set(['node_modules', '.git', '.next', 'dist', 'build', 'coverage', 'scripts']);

function readDirRecursive(dir, prefix = '') {
  const results = [];
  let items;
  try { items = fs.readdirSync(dir); } catch { return results; }
  for (const item of items) {
    if (SKIP_DIRS.has(item)) continue;
    const fullPath = path.join(dir, item);
    const relative = prefix ? `${prefix}/${item}` : item;
    try {
      const stat = fs.statSync(fullPath);
      if (stat.isDirectory()) {
        results.push(...readDirRecursive(fullPath, relative));
      } else {
        results.push(relative);
      }
    } catch { /* skip unreadable */ }
  }
  return results;
}

async function callBob(system, userMessage, maxTokens = 3000) {
  const res = await fetch(`${BOB_ENDPOINT}/messages`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', 'x-api-key': BOB_API_KEY },
    body: JSON.stringify({
      model: BOB_MODEL,
      max_tokens: maxTokens,
      system,
      messages: [{ role: 'user', content: userMessage }],
    }),
  });

  if (!res.ok) {
    const raw = await res.arrayBuffer();
    let errText;
    try { errText = zlib.gunzipSync(Buffer.from(raw)).toString('utf-8'); }
    catch { errText = Buffer.from(raw).toString('utf-8'); }
    throw new Error(`Bob API ${res.status}: ${errText}`);
  }

  const buffer = await res.arrayBuffer();
  let text;
  try { text = zlib.gunzipSync(Buffer.from(buffer)).toString('utf-8'); }
  catch { text = Buffer.from(buffer).toString('utf-8'); }

  const data = JSON.parse(text);
  return data.content?.[0]?.text ?? '';
}

async function main() {
  const srcDir = path.join(__dirname, '..');
  const structure = readDirRecursive(srcDir).slice(0, 120).join('\n');

  const KEY_FILES = [
    'src/lib/bob.ts',
    'src/lib/bob-explainer.ts',
    'src/app/api/search/route.ts',
    'src/app/api/explain/route.ts',
  ];

  const fileContents = KEY_FILES.map((f) => {
    try {
      const content = fs.readFileSync(path.join(srcDir, f), 'utf-8');
      return `// ${f}\n${content.slice(0, 2000)}`;
    } catch {
      return `// ${f} — not found`;
    }
  }).join('\n\n---\n\n');

  const system = `You are IBM Bob, an expert technical writer and developer advocate. You are helping a developer document their project for a hackathon submission. Generate a professional, detailed README.md for the RepoRadar project based on the project structure and code provided.

The README must include these sections in order:
1. Project title and one-line description
2. The problem it solves (3-4 sentences)
3. How it works (step by step, numbered)
4. How IBM Bob is integrated (be specific — reference the actual code and functions)
5. Tech stack (with versions)
6. Getting started (installation and running locally)
7. Architecture overview (describe the key files and their roles)
8. IBM Bob session report note (placeholder for where the exported Bob session report goes)
9. Hackathon submission notes

Write in clear, professional English. Use proper markdown formatting. Make the IBM Bob integration section especially detailed since this is a hackathon judged on Bob usage.`;

  const userMessage = `Project file structure:\n${structure}\n\nKey source files:\n${fileContents}`;

  console.log('Calling IBM Bob to generate README...');
  const readme = await callBob(system, userMessage);

  const outPath = path.join(srcDir, 'README.bob-generated.md');
  fs.writeFileSync(outPath, readme, 'utf-8');
  console.log('README generated by Bob. Review it at README.bob-generated.md, then copy to README.md when satisfied.');
}

main().catch((err) => {
  console.error('Error:', err.message);
  process.exit(1);
});
